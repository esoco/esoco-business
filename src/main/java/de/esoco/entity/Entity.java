	/**
	 * Returns a hash code that is consistent with the {@link #equals(Object)}
	 * implementation.
	 *
	 * @see Object#hashCode()
	 */
	@Override
	public int hashCode() {
		int hashCode = 37 * getClass().hashCode();

		if (hasFlag(MODIFIED)) {
			hashCode = 37 * hashCode + attributesHashCode();
		} else {
			long id = getId();

			hashCode = 37 * hashCode + (int) (id ^ (id >>> 32));
		}

		return hashCode;
	}

